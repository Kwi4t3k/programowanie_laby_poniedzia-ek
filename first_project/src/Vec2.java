// Klasa Vec2 reprezentuje wektor dwuwymiarowy.
public class Vec2 {
    // Zmienne x i y reprezentują współrzędne wektora.
    public double x, y;

    // Konstruktor klasy Vec2, który inicjalizuje współrzędne wektora.
    public Vec2(double x, double y) {
        this.x = x;
        this.y = y;
    }
}
